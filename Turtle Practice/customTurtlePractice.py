# import turtle graphics module
import turtle

# create a turtle
my_turtle = turtle.Turtle()
my_turtle.shape("turtle")
my_turtle.color("red")

# sample commands
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.right(90)

# end turtle progress
turtle.done()
